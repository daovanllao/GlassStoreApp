    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblThongKe.getModel();
        model.setRowCount(0);
        
        try {
            List<BaoCaoThongKe> list = dao.selectAll();
            for(BaoCaoThongKe bctk : list) {
                String trangT;
                if (bctk.getTrangThai() == true) {
                    trangT = "Đã Thanh Toán";
                } else {
                    trangT = "Chưa Được Thanh Toán";
                }
                Object[] row = {
                    bctk.getMaDH(),
                    bctk.getNgayLap(),
                    bctk.gettenKhachHang(),
                    bctk.getTongTien(),
                    trangT,
                    bctk.getTenSanPham(),
                    bctk.getDonGia(),
                    bctk.getSoLuong()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

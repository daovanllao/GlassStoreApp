void fillTableDoanhThu() {
        DefaultTableModel model = (DefaultTableModel) tblDoanhThu2.getModel();
        model.setRowCount(0);
        String thoiGian;
        
        try {
            List<BaoCaoThongKe> list = new ArrayList<>();
            if (chonTime == 2) {
                list = dao.selectAllDoanhThuThang();
            } else if (chonTime == 3) {
                list = dao.selectAllDoanhThuNam();
            } else if (chonTime == 1) {
                list = dao.selectAllDoanhThuNgay();
            }
            
            for(BaoCaoThongKe bctk : list) {
                Object[] row = {
                    bctk.getTenSanPham(),
                    bctk.getSoLuong(),
                    bctk.getDoanhThu()                   
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(""+e);
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu2!");
        }
    }
    

